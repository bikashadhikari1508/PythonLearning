dict1 = {"name": "Bikash",
         "DOB": 2000,
         "Place": "Mumbai",
         "telephone": 1779}

dict2 = {"name": "Root",
         "DOB": 2001,
         "Place": "Delhi",
         "telephone": 1889}
# Accessing elements of a dict
print(dict1)
print(dict2)

# Accessing a particular value
print(dict1['Place'])
print(dict2['name'])

# Get all keys
print(dict1.keys())
print(dict2.keys())

# Get all values
print(dict1.values())
print(dict2.values())

# Get all key-value pair
print(dict1.items())
print(dict2.items())

